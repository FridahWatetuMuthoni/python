import math

pi = 3.14
num = -10

# rounding of a number to the nearest whole number
print(round(pi))

# rounding a number up and down
print(math.floor(pi))
print(math.ceil(pi))

# return the positive value of a number
print(abs(num))

# raising the base to the power of the exponent
print(pow(10, 2))

# squareroot of a function
print(math.sqrt(64))

# finnding the maximum value
print(max(10, 20))
print(min(51, 57, 41))
